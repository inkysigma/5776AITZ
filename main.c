task usercontrol() {
	datalogClear();
	pid config;
	pid rconfig;
	config.kp = 10;
	config.ki = 0;
	config.kd = 0;
	config.dt = 20;
	rconfig.kp = config.kp;
	rconfig.ki = config.ki;
	rconfig.kd = config.kd;
	rconfig.dt = config.dt;

	rconfig.kp = 10;
	rconfig.ki = 0;
	rconfig.kd = 0;
	rconfig.dt = 20;

	setLeftConfig(config);
	setRightConfig(rconfig);

	startLeftLiftPid();

	while (true) {
		// drive code
		moveDrive(vexRT[Ch3], vexRT[Ch2]);

		// lift code
		if (vexRT[Btn5U]) {
			stopPid();
			moveRight(127);
			} else if (vexRT[Btn5D]) {
			stopPid();
			moveRight(-127);
			} else {
			if (!isRunning()) {
				moveRight(0);
			}
		}

		if (vexRT[Btn6U]) {
			openClaw(127);
			} else if (vexRT[Btn6D]) {
			closeClaw(127);
			} else {
			stopClaw();
		}

		if (vexRT[Btn8L]) {
			moveGoal(100);
			} else if (vexRT[Btn8R]) {
			moveGoal(-100);
			} else {
			moveGoal(0);
		}

		if (vexRT[Btn7U]) {
			moveSwitchLift(127);
			} else if (vexRT[Btn7D]) {
			moveSwitchLift(-127);
			} else {
			moveSwitchLift(0);
		}
		if (vexRT[Btn7L]) {
			startPid(SensorValue[LeftLiftPot], rconfig);
			} else if (vexRT[Btn7R]) {
			stopPid();
		}

		if (SensorValue[LeftLiftButton]) {
			SensorValue[LeftLift] = 0;
		}
		if (SensorValue[RightLiftButton]) {
			SensorValue[RightLift] = 0;
		}
	}
}
